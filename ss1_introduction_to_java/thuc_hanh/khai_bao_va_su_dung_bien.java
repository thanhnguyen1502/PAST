package ss1_introduction_to_java.thuc_hanh;

public class khai_bao_va_su_dung_bien {
    public static void main(String[] args) {
        int i = 10; // kiểu int là: dùng để lưu kiểu số nguyên
        float f = 20.5f;// kiểu float là: kiểu dữ liệu lưu số thực
        double d = 20.5;// kiểu double là: kiểu dữ liệu lưu số thực (rộng hơn float)
        boolean b = true;//
        char c = 'a';// kiểu char: dùng để lưu ký tự và số nguyên không âm
        String  s = "Hà Nội";

        System.out.println("i = " + i);
        System.out.println("f = " + f);
        System.out.println("d = " + d);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        System.out.println("s = " + s);

    }
}
